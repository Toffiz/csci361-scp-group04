// Helper function to translate text
export function translateData(text: string, locale: Locale): string {
  // Try product names first
  if (productNames[text]) {
    return productNames[text][locale];
  }
  
  // Try descriptions
  if (productDescriptions[text]) {
    return productDescriptions[text][locale];
  }
  
  // Try company names
  if (companyNames[text]) {
    return companyNames[text][locale];
  }
  
  // Return original if no translation found
  return text;
}

